let hora = 24;
if (hora > 0 && hora <= 10) {
  console.log("Buenos días");
} else if (hora > 10 && hora <= 18) {
  console.log("Buenas tardes");
} else if (hora > 18 && hora <= 24) {
  console.log("Buenas noches");
} else {
  console.log("Hora no válida");
}

